package br.edu.ifpr.inf.aula02;

public class Produto {

	public static void main(String[] args) {
		/*
		 * Tudo que for texto vai ser da classe STRING Caso você veja a necessidade,
		 * você pode fazer outra palavra com a letra maiúscula para complementar.
		 * 
		 * Aparentemente o "String" é diferente de todas as outras classes, ele faz
		 * parte do tipo especial.
		 */
		String nomeProd;
		String marcaProd;
		double valorPrdo;
		int quantidadeProd;
		boolean situProd;
		/*
		 * 'double' serve para salvar números que podem ter valores mais específicos.
		 * 'int' serve para salvar quantidades unitárias Tipo "R$6,20" 'boolean' é a
		 * classe usada para pode rdefinir "verdadeiro" ou "falso".
		 */
		nomeProd = "Livro - O manifesto Comunista.";
		marcaProd = "Editora Avante";
		valorPrdo = 19.90;
		quantidadeProd = 10;
		situProd = true;
		// Ao contrário de classes, nomes de variáveis começam com a primeira letra
		// minúscula.
	}

}
